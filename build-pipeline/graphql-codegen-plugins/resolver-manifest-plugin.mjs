/**
 * graphql-codegen plugin: resolver-manifest-plugin
 *
 * Scans a types directory for .resolvers.ts and .permissions.ts files and generates
 * a TypeScript module with static imports and exports for each. This replaces the
 * runtime glob pattern in resolver-builder.ts (loadResolversFromGlob) so rolldown
 * can statically analyse all resolver imports at bundle time.
 *
 * Config options:
 *   typesDir  - path relative to repo root containing the resolver/permissions files
 *   outputFile - path relative to repo root where this file will be written
 *               (used to compute correct relative import paths)
 */

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const REPO_ROOT = path.resolve(__dirname, '../..');

export const plugin = async (_schema, _documents, config, info) => {
	const { typesDir } = config;

	if (!typesDir) {
		throw new Error('resolver-manifest-plugin: "typesDir" config option is required');
	}

	const absoluteTypesDir = path.resolve(REPO_ROOT, typesDir);

	// Use the output file path from codegen info to compute relative imports
	const outputFile = info?.outputFile
		? path.resolve(REPO_ROOT, info.outputFile)
		: path.resolve(absoluteTypesDir, '../builder/resolver-manifest.generated.ts');

	const resolverFiles = await collectFiles(absoluteTypesDir, '.resolvers.ts');
	const permissionFiles = await collectFiles(absoluteTypesDir, '.permissions.ts');

	const resolverImports = resolverFiles.map(
		(filePath, index) => `import resolver${index} from '${toImportPath(outputFile, filePath)}';`,
	);
	const permissionImports = permissionFiles.map(
		(filePath, index) => `import permission${index} from '${toImportPath(outputFile, filePath)}';`,
	);

	const allImports = [...resolverImports, ...permissionImports];

	const lines = [
		'// This file is auto-generated by graphql-codegen (resolver-manifest-plugin).',
		'// Do not edit directly.',
		...(allImports.length === 0 ? [] : ['', ...allImports]),
		'',
		'export const ocomGraphqlResolvers = [',
		...resolverFiles.map((_filePath, index) => `\tresolver${index},`),
		'];',
		'',
		'export const ocomGraphqlPermissions = [',
		...permissionFiles.map((_filePath, index) => `\tpermission${index},`),
		'];',
		'',
	];

	return lines.join('\n');
};

async function collectFiles(rootDir, suffix) {
	const entries = await fs.readdir(rootDir, { withFileTypes: true });
	const files = await Promise.all(
		entries.map(async (entry) => {
			const entryPath = path.join(rootDir, entry.name);
			if (entry.isDirectory()) {
				return collectFiles(entryPath, suffix);
			}
			if (entry.isFile() && entry.name.endsWith(suffix)) {
				return [entryPath];
			}
			return [];
		}),
	);
	return files.flat().sort((a, b) => a.localeCompare(b));
}

function toImportPath(fromFile, toFile) {
	const relativePath = path.relative(path.dirname(fromFile), toFile);
	return normalizeRelativeSpecifier(relativePath);
}

function normalizeRelativeSpecifier(value) {
	const posixValue = toPosixPath(value);
	return posixValue.startsWith('.') ? posixValue : `./${posixValue}`;
}

function toPosixPath(value) {
	return value.split(path.sep).join('/');
}
