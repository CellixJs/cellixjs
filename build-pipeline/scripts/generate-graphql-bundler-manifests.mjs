import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '../..');

const coreSchemaDir = path.join(repoRoot, 'packages/cellix/graphql-core/src/schema');
const ocomSchemaDir = path.join(repoRoot, 'packages/ocom/graphql/src/schema');

const generatedFiles = [
	{
		rootDir: coreSchemaDir,
		pattern: '.graphql',
		outputFile: path.join(coreSchemaDir, 'base-type-defs.generated.ts'),
		exportName: 'baseCellixTypeDefs',
	},
	{
		rootDir: path.join(ocomSchemaDir, 'types'),
		pattern: '.graphql',
		outputFile: path.join(ocomSchemaDir, 'builder/schema-type-defs.generated.ts'),
		exportName: 'ocomGraphqlTypeDefs',
	},
];

async function main() {
	for (const generatedFile of generatedFiles) {
		const filePaths = await collectFiles(generatedFile.rootDir, generatedFile.pattern);
		const fileContents = await Promise.all(
			filePaths.map(async (filePath) => ({
				filePath,
				content: await fs.readFile(filePath, 'utf8'),
			})),
		);
		const output = [
			'// This file is auto-generated by build-pipeline/scripts/generate-graphql-bundler-manifests.mjs.',
			'// Do not edit directly.',
			'',
			`export const ${generatedFile.exportName} = [`,
			...fileContents.map(({ filePath, content }) => `\t${JSON.stringify(content)}, // ${toPosixPath(path.relative(repoRoot, filePath))}`),
			'] as const;',
			'',
		].join('\n');
		await writeIfChanged(generatedFile.outputFile, output);
	}

	await writeResolverManifest();
}

async function writeResolverManifest() {
	const resolverFiles = await collectFiles(path.join(ocomSchemaDir, 'types'), '.resolvers.ts');
	const permissionFiles = await collectFiles(path.join(ocomSchemaDir, 'types'), '.permissions.ts');
	const outputFile = path.join(ocomSchemaDir, 'builder/resolver-manifest.generated.ts');

	const importLines = [
		...resolverFiles.map((filePath, index) => `import resolver${index} from '${toImportPath(outputFile, filePath)}';`),
		...permissionFiles.map((filePath, index) => `import permission${index} from '${toImportPath(outputFile, filePath)}';`),
	];

	const output = [
		'// This file is auto-generated by build-pipeline/scripts/generate-graphql-bundler-manifests.mjs.',
		'// Do not edit directly.',
		...(importLines.length === 0 ? [] : ['', ...importLines]),
		'',
		'export const ocomGraphqlResolvers = [',
		...resolverFiles.map((_filePath, index) => `\tresolver${index},`),
		'];',
		'',
		'export const ocomGraphqlPermissions = [',
		...permissionFiles.map((_filePath, index) => `\tpermission${index},`),
		'];',
		'',
	].join('\n');

	await writeIfChanged(outputFile, output);
}

async function collectFiles(rootDir, suffix) {
	const entries = await fs.readdir(rootDir, { withFileTypes: true });
	const files = await Promise.all(
		entries.map(async (entry) => {
			const entryPath = path.join(rootDir, entry.name);
			if (entry.isDirectory()) {
				return collectFiles(entryPath, suffix);
			}
			if (entry.isFile() && entry.name.endsWith(suffix)) {
				return [entryPath];
			}
			return [];
		}),
	);

	return files.flat().sort((left, right) => left.localeCompare(right));
}

async function writeIfChanged(filePath, content) {
	const existing = await fs.readFile(filePath, 'utf8').catch(() => undefined);
	if (existing === content) {
		return;
	}

	await fs.mkdir(path.dirname(filePath), { recursive: true });
	await fs.writeFile(filePath, content);
}

function toImportPath(fromFile, toFile) {
	const relativePath = path.relative(path.dirname(fromFile), toFile);
	return normalizeRelativeSpecifier(relativePath);
}

function normalizeRelativeSpecifier(value) {
	const posixValue = toPosixPath(value);
	return posixValue.startsWith('.') ? posixValue : `./${posixValue}`;
}

function toPosixPath(value) {
	return value.split(path.sep).join('/');
}

await main();
