/**
 * graphql-codegen plugin: static-type-defs-plugin
 *
 * Reads all .graphql SDL files from a configurable source directory and outputs
 * a TypeScript constant containing the raw SDL strings as an array. This replaces
 * runtime glob-based loading (loadFilesSync) with a statically analysable import,
 * enabling rolldown to bundle the API without dynamic file discovery at startup.
 *
 * Config options:
 *   sourceDir  - path relative to repo root that contains the .graphql files to collect
 *   exportName - name of the exported TypeScript constant
 */

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { collectFiles, toPosixPath } from './file-utils.mjs';

export const plugin = async (_schema, _documents, config) => {
	const { sourceDir, exportName } = config;

	if (!sourceDir || !exportName) {
		throw new Error('static-type-defs-plugin: "sourceDir" and "exportName" config options are required');
	}

	const absoluteSourceDir = path.resolve(process.cwd(), sourceDir);
	const files = await collectFiles(absoluteSourceDir, '.graphql');
	const fileContents = await Promise.all(
		files.map(async (filePath) => ({
			filePath,
			content: await fs.readFile(filePath, 'utf8'),
		})),
	);

	const lines = [
		'// This file is auto-generated by graphql-codegen (static-type-defs-plugin).',
		'// Do not edit directly.',
		'',
		`export const ${exportName} = [`,
		...fileContents.map(({ filePath, content }) => `\t${JSON.stringify(content)}, // ${toPosixPath(path.relative(process.cwd(), filePath))}`),
		'] as const;',
		'',
	];

	return lines.join('\n');
};
