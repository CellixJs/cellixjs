#!/usr/bin/env node

import { promises as fs } from 'node:fs';
import path from 'node:path';

const SCHEMA_SUFFIX = '.schema.json';
const GENERATED_SUFFIX = '.schema.generated.ts';
const DEFAULT_TARGET_DIR = 'src';
const SAFE_TARGET_DIR_PATTERN = /^[A-Za-z0-9_-]+(?:\/[A-Za-z0-9_-]+)*$/;

async function main() {
	const args = process.argv.slice(2);
	const targetDirs = args.length > 0 ? args : [DEFAULT_TARGET_DIR];
	const workspaceRoot = process.cwd();

	const schemaFiles = [];
	for (const targetDir of targetDirs) {
		const resolvedDir = resolveTargetDir(workspaceRoot, targetDir);
		schemaFiles.push(...(await findSchemaFiles(resolvedDir)));
	}

	await Promise.all(schemaFiles.map((schemaFile) => generateSchemaModule(schemaFile)));
}

function resolveTargetDir(workspaceRoot, targetDir) {
	if (!SAFE_TARGET_DIR_PATTERN.test(targetDir)) {
		throw new Error(`Schema scan path must be a relative workspace subdirectory like "${DEFAULT_TARGET_DIR}" or "${DEFAULT_TARGET_DIR}/schemas": ${targetDir}`);
	}

	return path.join(workspaceRoot, ...targetDir.split('/'));
}

async function findSchemaFiles(targetDir) {
	const entries = await fs.readdir(targetDir, { withFileTypes: true });
	const schemaFiles = [];

	for (const entry of entries) {
		const entryPath = path.join(targetDir, entry.name);
		if (entry.isDirectory()) {
			schemaFiles.push(...(await findSchemaFiles(entryPath)));
			continue;
		}
		if (entry.isFile() && entry.name.endsWith(SCHEMA_SUFFIX) && (await hasSiblingQueueDefinition(entryPath))) {
			schemaFiles.push(entryPath);
		}
	}

	return schemaFiles;
}

async function hasSiblingQueueDefinition(schemaFile) {
	const queueDefinitionFile = `${schemaFile.slice(0, -SCHEMA_SUFFIX.length)}.ts`;
	try {
		await fs.access(queueDefinitionFile);
		return true;
	} catch {
		return false;
	}
}

async function generateSchemaModule(schemaFile) {
	const rawSchema = await fs.readFile(schemaFile, 'utf8');
	const schema = JSON.parse(rawSchema);
	const generatedFile = schemaFile.slice(0, -SCHEMA_SUFFIX.length) + GENERATED_SUFFIX;
	const fileContents = renderGeneratedModule(schema);
	await fs.writeFile(generatedFile, fileContents);
}

function renderGeneratedModule(schema) {
	const renderedSchema = JSON.stringify(schema, null, '\t');

	return `// This file is auto-generated by cellix-generate-queue-schema-types.
// Do not edit it directly. Update the sibling .schema.json file instead.

import type { FromSchema, JSONSchema } from '@cellix/service-queue-storage';

export const schema = ${renderedSchema} as const satisfies JSONSchema;

export type Schema = FromSchema<typeof schema>;
`;
}

main().catch((error) => {
	console.error(error);
	process.exitCode = 1;
});

export { findSchemaFiles, renderGeneratedModule, resolveTargetDir };
