import { mkdir, mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { afterEach, describe, expect, it } from 'vitest';

const scriptPath = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../bin/generate-queue-schema-types.mjs');
const tempDirs: string[] = [];

afterEach(async () => {
	await Promise.all(tempDirs.map((dir) => rm(dir, { recursive: true, force: true })));
	tempDirs.length = 0;
});

describe('cellix-generate-queue-schema-types', () => {
	it('generates a typed schema module from colocated .schema.json files', async () => {
		const tempDir = await mkdtemp(path.join(os.tmpdir(), 'queue-schema-codegen-'));
		tempDirs.push(tempDir);

		const schemasDir = path.join(tempDir, 'src', 'schemas', 'outbound');
		await mkdir(schemasDir, { recursive: true });
		await writeFile(path.join(schemasDir, 'order-created.ts'), 'export const orderCreatedQueue = null;\n');
		await writeFile(
			path.join(schemasDir, 'order-created.schema.json'),
			JSON.stringify(
				{
					$schema: 'http://json-schema.org/draft-07/schema#',
					title: 'OrderCreatedPayload',
					type: 'object',
					properties: {
						orderId: { type: 'string' },
					},
					required: ['orderId'],
					additionalProperties: false,
				},
				null,
				'\t',
			),
		);

		const { execaNode } = await import('execa');
		await execaNode(scriptPath, ['src/schemas'], { cwd: tempDir });

		const generatedFile = path.join(schemasDir, 'order-created.schema.generated.ts');
		const generatedContents = await readFile(generatedFile, 'utf8');

		expect(generatedContents).toContain('auto-generated by cellix-generate-queue-schema-types');
		expect(generatedContents).toContain("import type { FromSchema, JSONSchema } from '@cellix/service-queue-storage';");
		expect(generatedContents).toContain('export const schema = {');
		expect(generatedContents).toContain('export type Schema = FromSchema<typeof schema>;');
		expect(generatedContents).toContain('"orderId"');
	});

	it('skips schema files without a sibling queue definition module', async () => {
		const tempDir = await mkdtemp(path.join(os.tmpdir(), 'queue-schema-codegen-'));
		tempDirs.push(tempDir);

		const schemasDir = path.join(tempDir, 'src', 'schemas', 'inbound');
		await mkdir(schemasDir, { recursive: true });
		await writeFile(
			path.join(schemasDir, 'unreferenced.schema.json'),
			JSON.stringify(
				{
					type: 'object',
					properties: {
						id: { type: 'string' },
					},
				},
				null,
				'\t',
			),
		);

		const { execaNode } = await import('execa');
		await execaNode(scriptPath, ['src/schemas'], { cwd: tempDir });

		await expect(readFile(path.join(schemasDir, 'unreferenced.schema.generated.ts'), 'utf8')).rejects.toThrow();
	});

	it('rejects schema scan paths outside the current workspace', async () => {
		const tempDir = await mkdtemp(path.join(os.tmpdir(), 'queue-schema-codegen-'));
		tempDirs.push(tempDir);

		const { execaNode } = await import('execa');
		await expect(execaNode(scriptPath, ['../outside'], { cwd: tempDir })).rejects.toMatchObject({
			stderr: expect.stringContaining('Schema scan path must be a relative workspace subdirectory'),
		});
	});
});
